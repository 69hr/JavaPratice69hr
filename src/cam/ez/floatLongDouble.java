package cam.ez;

public class floatLongDouble
{
    public static void main(String[] args)
    {
        int a = 69;

        long b = 99999999999999999L;

        float c = 69.420F;

        double d = 696969696969.420;

        System.out.println(a);
        //this will print out the int

        System.out.println(b);
        //this will print out the long

        System.out.println(c);
        //this will print out the float

        System.out.println(d);
        //this will print out the double

    }

}
